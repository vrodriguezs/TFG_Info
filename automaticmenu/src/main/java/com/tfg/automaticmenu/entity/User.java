package com.tfg.automaticmenu.entity;

public class User {

    String name;
    String exRoutine;
    String exIntensity;
    String sex;
    int age;
    int weight;
    int height;
    Boolean veg;
    int dishes;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getExRoutine() {
        return exRoutine;
    }

    public void setExRoutine(String exRoutine) {
        this.exRoutine = exRoutine;
    }

    public String getExIntensity() {
        return exIntensity;
    }

    public void setExIntensity(String exIntensity) {
        this.exIntensity = exIntensity;
    }

    public String getSex() {
        return sex;
    }

    public void setSex(String sex) {
        this.sex = sex;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getWeight() {
        return weight;
    }

    public void setWeight(int weight) {
        this.weight = weight;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public Boolean getVeg() {
        return veg;
    }

    public void setVeg(Boolean veg) {
        this.veg = veg;
    }

    public int getDishes() {
        return dishes;
    }

    public void setDishes(int dishes) {
        this.dishes = dishes;
    }
}
